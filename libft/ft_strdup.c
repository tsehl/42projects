#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*copy;
	char	*start;

	copy = (char *)malloc(sizeof(*copy) * (ft_strlen(s) + 1));
	if (!copy)
		return (NULL);
	start = copy;
	while (*s)
	{
		*copy = *s;
		copy++;
		s++;
	}
	*copy = '\0';
	return (start);
}

char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] > 96 && str[i] < 123) || (str[i] > 64 && str[i] < 91)
			|| (str[i] < 58 && str[i] > 47))
		{
			if (str[i] > 96 && str[i] < 123)
				str[i] -= 32;
			i++;
			while (str[i] && ((str[i] > 96 && str[i] < 123)
					|| (str[i] > 64 && str[i] < 91)
					|| (str[i] > 47 && str[i] < 58)))
			{
				if (str[i] > 64 && str[i] < 91)
					str[i] = str[i] + 32;
				i++;
			}
		}
		else
			i++;
	}
	return (str);
}

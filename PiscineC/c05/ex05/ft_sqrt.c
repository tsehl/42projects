int	ft_sqrt(int nb)
{
	long long int	x;

	x = 1;
	if (nb == 1)
		return (1);
	if (nb == 4)
		return (2);
	if (nb == 9)
		return (3);
	if (nb > 0)
	{
		while (x <= nb / 4)
		{
			if (x * x == nb)
				return (x);
			x++;
		}
	}
	return (0);
}

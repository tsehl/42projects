#include <unistd.h>

void	print_comb(int n1, int n2)
{
	char	s1[2];
	char	s2[2];

	s1[0] = n1 / 10 + 48;
	s1[1] = n1 % 10 + 48;
	s2[0] = n2 / 10 + 48;
	s2[1] = n2 % 10 + 48;
	write(1, s1, 2);
	write(1, " ", 1);
	write(1, s2, 2);
}

void	ft_print_comb2(void)
{
	char	s1[2];
	char	s2[2];
	int		n1;
	int		n2;

	n1 = 0;
	while (n1 <= 98)
	{
		n2 = n1 + 1;
		while (n2 <= 99)
		{
			print_comb(n1, n2);
			if (!(n1 == 98 && n2 == 99))
				write(1, ", ", 2);
			n2++;
		}
		n1++;
	}
}
